class Champion {
    constructor(name) {
        let randomQuote = getRandInd(0, 1);

        this.name = name;
        this.splashURL = `https://ddragon.leagueoflegends.com/cdn/img/champion/splash/${this.name}_0.jpg`;
        this.quote = quoteData[this.name]["quotes"][randomQuote];
        this.soundbite = quoteData[this.name]["soundbites"][randomQuote];
    }
}

function getRandInd(min, max) {
    min = Math.ceil(min);
    max = Math.floor(max);
    return Math.floor(Math.random() * (max - min + 1)) + min;
}

function playRandQuote() {
    let randChampInd = getRandInd(0, champArr.length - 1);
    let champion = new Champion(champArr[randChampInd]);
    if (!soundbite) return;
    splash.src = champion.splashURL;
    document.getElementById("championname").innerHTML = champion.name;
    document.getElementById("championquote").innerHTML = champion.quote;
    soundbite.src = champion.soundbite;
    soundbite.play();
}

let champArr = Object.keys(quoteData);
const soundbite = document.querySelector("audio");
const splash = document.getElementById("splash");

playRandQuote();

newquote.onclick = () => {
    playRandQuote();
};